function H = Hone(U)
    n = size(U,1);
    h=1/(n-1);
    U1 = U(1:n-1,1:n-1)+U(1:n-1,2:n)+U(2:n,1:n-1)+U(2:n,2:n);
    U2 = U(1:n-1,2:n)+U(2:n,1:n-1)-U(1:n-1,1:n-1)-U(2:n,2:n);
    U3 = U(2:n,1:n-1)+U(2:n,2:n)-U(1:n-1,1:n-1)-U(1:n-1,2:n);
    H = h^2*U1.^2./16 + U2.^2./4+U3.^2./4;
    H = sqrt(sum(H(:)));
end